/*2. Write a program to calculate the sum of the first 10 natural numbers. */

public class Groupb1b{
    public static void main(String[]args){
        int i, num=10, sum=0;
        for(i=1;i<=num;++i){
            sum=sum+i;
        }
        System.out.println(" Sum of first ten number is = " +sum);
    }
}